<section class="sobremais container">
<h1 class="titulo">O que é o SyncLab</h1>
<h2 class="subtitulo">Um projeto de iniciativa estudantil</h2>
<p>
    O SyncLab começou com alunos do Cefet que participavam de projetos de
    extensão que queriam uma forma mais fácil e pratica de organizar certas
    atividades.
</p>
<p>
    O SyncLab é um projeto que visa desenvolver um software voltado para gestão de
    laboratórios de pesquisa e desenvolvimento de quaisquer instituições.
</p>
<p>
    Laboratórios são lugares que geralmente possuem materiais valiosos,
    sejam eles de pesquisa ou os próprios aparelhos.
    Portanto, um monitoramento rígido desses lugares, saber quem entra e quem sai,
    quais pessoas têm acesso a chave, e também quem pode ter acesso ao sistema e
    se conectar a rede caso seja um laboratório de informática é importante.
    O propósito do SyncLab é justamente auxiliar nesse monitoramento.
</p>
<p>
    A princípio, decidimos qual metodologia usaríamos para o desenvolvimento
    do projeto, onde optamos pelo Scrum por suas características ágeis e
    por melhor se adaptar ao tempo que tínhamos para desenvolver entre as disciplinas
    e a vida pessoal. Com isso definido, dividimos a equipe em alguns times,
    os quais estariam responsáveis por partes diferentes do projeto.
</p>
<p>
    Em um primeiro momento as atividades a serem desenvolvidas
    foram Banco de Dados, Back-end e a Prototipação das Telas.
</p>
<p>
    Como o projeto Synclab é um projeto interdisciplinar no Eixo de Engenharia
    de Software do CEFET-MG, boa parte do desenvolvimento do Banco de Dados
    para o sistema foi desenvolvido na matéria de Banco de Dados. O Back-end foi feito
    durante a matéria de Engenharia de Software II, e por fim a Prototipação das Telas e mais tarde
    o Front-end foi desenvolvido para a matéria de Desenvolvimento Web I.
</p>
